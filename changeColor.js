function usernameMouseOver() {
    document.getElementById("username").style.color = "blue";
}

function usernameMouseOut() {
    document.getElementById("username").style.color = "black";
}
function passwordMouseOver() {
	document.getElementById("password").style.color = "blue";
}
function passwordMouseOut() {
	document.getElementById("password").style.color = "black";
}
